        $this->end_controls_section();
    }


	public function render() {
		$settings  = $this->get_settings_for_display(); 
		extract($settings);

	   	$bg_src = wp_get_attachment_image_src( $settings['background_bg']['id'], 'full' );
		$bg_url = $bg_src ? $bg_src[0] : ''; 

		?>
        <section class="about-area bdevs-el-content ">
            <div class="container">
                <div class="row align-items-center">
                    <div class="col-xl-6 col-lg-6">
                        <div class="medical-icon-brand pos-rel f-left">
                            <img src="<?php print esc_url($bg_url); ?>" alt="Experience Background">
                        </div>
                        <div class="about-title mb-20 fix">
                        	<?php 
                        	if (!empty($title)) : ?>
						        <h2 class="sec-title mb-40 bdevs-el-title "><?php print wp_kses_post( $title ); ?></h2>
						    <?php 
							endif; ?> 

							<?php 
							if (!empty($sub_title)) : ?>
				                <h5 class="m-0 bdevs-el-subtitle"><?php print wp_kses_post( $sub_title ); ?></h5>
				            <?php 
				        	endif; ?>
                        </div>
                    </div>
                    <div class="col-xl-6 col-lg-6">
                        <div class="about-right-content">
                            <p class="bdevs-el-desc" style="<?php print esc_attr( $desc_font_size ); ?>;  <?php print esc_attr( $desc_color ); ?>;"><?php print wp_kses_post( $desc ); ?></p>
                        </div>
                    </div>
                </div>
            </div>
        </section>
	<?php
	}

